# Test-driven development or TDD is an alternative programming practice in which the tests are written first and the code is written so that the tests will pass

# Step 1, you write a test for a feature that fails. In Step 2, you write code in accordance with the tests. Step 3 requires that you run the test expecting them to fail. In Step 4, you evaluate the error and refactor the code as needed. Finally, in Step 5, you rerun the process


# This process is also called the red-green-refactor cycle. Red implies the failed tests and green shows the passing tests after refactoring. The whole point of following this cycle is to fail the tests and rewrite until you don't have to. A feature is complete when everything is green and you no longer need to rerun

